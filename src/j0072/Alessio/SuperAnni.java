/* Questa classe verra' trattata 
 * per sommare e moltiplicare l'eta'. */
public class SuperAnni {

	public SuperAnni(long valore1, long valore2) {

	}

	public long Somma(long valoreA, long valoreB) {
		return valoreB + valoreA;
	}

	public long Moltiplica (long valore1, long valore2) {
		return valore1 * valore2;
	}

}